import sys
input = sys.stdin.readline

N = int(input())

# N이 90까지 입력될 수 있으므로, 미리 배열을 넉넉하게 잡거나 N에 맞춰 초기화
dp = [0] * (N + 1)

# 초기값 설정: 1자리 이친수는 '1' 하나뿐이고, 2자리 이친수는 '10' 하나뿐임
dp[1] = 1
if N >= 2:
    dp[2] = 1

for i in range(3, N + 1):
    # 끝자리가 0이면 앞자리는 0, 1 둘 다 가능하고, 끝자리가 1이면 앞자리는 무조건 0이어야 함
    # 결과적으로 dp[i] = (i-1번째 개수) + (i-2번째 개수) 라는 점화식이 성립 (피보나치)
    dp[i] = dp[i - 1] + dp[i - 2]

print(dp[N])